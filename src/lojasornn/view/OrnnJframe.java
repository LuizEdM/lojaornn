    private void BotaoMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoMostrarActionPerformed
        ArmaController botaomost = new ArmaController();
        
        ArrayList<Arma> listaArma = botaomost.listarArma();
        
        DefaultTableModel tabela = (DefaultTableModel) TabelaMostrar.getModel();
        
        String[] coluna  = {"","","",""};
        for(Arma arma:listaArma){
        
        coluna[0] = String.valueOf(arma.getTipoArma());
        coluna[1] = String.valueOf(arma.getNome());
        coluna[2] = String.valueOf(arma.getPoder());
        coluna[3] = String.valueOf(arma.getCusto());
        tabela.addRow(coluna);
    }//GEN-LAST:event_BotaoMostrarActionPerformed
    }
